# We will shortly discuss about the comments in Python.
#? Comments are used to explain the code. It makes the code more readable.
#* Comments are ignored by the Python interpreter.
#* You can write comments in Python using the # symbol.
#* For example:
# This is a comment
print("Hello, World!")
#* In this example, the print() function will be executed, but the comment will be ignored.
#* You can also write comments on the same line as code.
#* For example:
print("Hello, World!") # This is a comment

#* You can also write multiline comments using triple quotes.
#* For example:
""" 
This is a multiline comment"
"""
#* In this example, the comment will be ignored by the Python interpreter.

#* Comments are very useful in programming. They help you to understand the code better.