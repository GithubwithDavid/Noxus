# TODO: Explain what is high level, low level, interpreted, compiled, dynamically typed, statically typed, cross-platform language, object-oriented, procedural, functional, and scripting language.

#? High-level language: A high-level language is a programming language that is designed to be easy for humans to read and write. It is closer to human language than machine language.

#! Low-level language: A low-level language is a programming language that is closer to machine language than human language. It is more difficult to read and write than high-level languages.

#? Interpreted language: An interpreted language is a programming language that is executed line by line. The interpreter reads the code and executes it immediately.

#! Compiled language: A compiled language is a programming language that is translated into machine code before execution. The compiler reads the code and generates an executable file.

#? Dynamically typed language: A dynamically typed language is a programming language that does not require you to declare the type of a variable. The type of a variable is determined at runtime.

#! Statically typed language: A statically typed language is a programming language that requires you to declare the type of a variable. The type of a variable is determined at compile time.

#? Cross-platform language: A cross-platform language is a programming language that can run on different platforms without modification. Python is a cross-platform language.

#? Object-oriented language: An object-oriented language is a programming language that supports object-oriented programming. Objects are used to represent data and behavior.

